"""Generate code from the spec: a runnable agent skeleton wired to the eval
harness, so a team goes from "diagram" to "failing tests they can make pass".

`scaffold()` returns a mapping of relative path -> file contents. The pipeline
writes these to disk. We keep generation deterministic (templates over the
typed spec) so the output is reviewable and stable in version control; the LLM
is used only to enrich docstrings/TODOs when online.
"""

from __future__ import annotations

import keyword
import re

from .llm import LLMClient
from .schema import Component, ComponentType, SystemSpec

_HEADER = (
    '"""Auto-generated by agentic-ai-eval. Edit the TODOs to implement.\n\n'
    "This is a skeleton: each component is a method you fill in. The eval harness\n"
    'in eval_harness.py runs the generated suite against this agent."""'
)


def scaffold(spec: SystemSpec, *, client: LLMClient | None = None) -> dict[str, str]:
    client = client or LLMClient()
    files = {
        "agent.py": _agent_module(spec),
        "eval_harness.py": _harness_module(spec),
        "README.md": _readme(spec),
    }
    return files


# --------------------------------------------------------------------------- #
# agent.py
# --------------------------------------------------------------------------- #


def _agent_module(spec: SystemSpec) -> str:
    cls = _class_name(spec.name)
    methods: list[str] = []
    for comp in spec.components:
        methods.append(_component_method(comp))

    pipeline_body = _pipeline_body(spec)

    return f'''{_HEADER}

from __future__ import annotations

from dataclasses import dataclass, field


@dataclass
class AgentResult:
    """What the agent returns for one input — shaped to build an eval Trace."""

    output: str = ""
    tool_calls: list[str] = field(default_factory=list)
    steps: int = 0
    input_tokens: int = 0
    output_tokens: int = 0


class {cls}:
    """Skeleton for: {spec.name}.

    Goal: {spec.goal or spec.summary or "TODO: state the goal"}
    """

    def run(self, user_input: str) -> AgentResult:
        """End-to-end entrypoint. Wire the components together here."""
        result = AgentResult()
{pipeline_body}
        return result

{chr(10).join(methods)}
'''


def _component_method(comp: Component) -> str:
    safe = _safe_ident(comp.id)
    doc = comp.description or comp.name
    body = _component_body(comp)
    return f'''    def {safe}(self, state: dict) -> dict:
        """[{comp.type.value}] {doc}"""
{body}
'''


def _component_body(comp: Component) -> str:
    if comp.type == ComponentType.TOOL_USE and comp.tools:
        tools = ", ".join(repr(t.name) for t in comp.tools)
        return (
            f"        # TODO: choose among tools [{tools}] and call as needed.\n"
            "        # Record each call name into state['tool_calls'].\n"
            "        raise NotImplementedError\n"
        )
    if comp.type == ComponentType.RETRIEVAL:
        return (
            "        # TODO: retrieve grounding passages; put them in state['context'].\n"
            "        # Faithfulness eval will check every claim is supported by this.\n"
            "        raise NotImplementedError\n"
        )
    if comp.type == ComponentType.GUARDRAIL:
        return (
            "        # TODO: apply policy. Return state unchanged if allowed, or set\n"
            "        # state['refused'] = True with a safe message.\n"
            "        raise NotImplementedError\n"
        )
    return "        # TODO: implement.\n        raise NotImplementedError\n"


def _pipeline_body(spec: SystemSpec) -> str:
    if not spec.components:
        return "        # TODO: no components parsed; implement directly.\n        result.output = ''\n"
    lines = ["        state = {'input': user_input, 'tool_calls': [], 'context': ''}"]
    # Walk components in declared order (ingest orders them along the flow).
    for comp in spec.components:
        safe = _safe_ident(comp.id)
        lines.append(f"        state = self.{safe}(state)  # {comp.type.value}")
    lines.append("        result.output = str(state.get('output', ''))")
    lines.append("        result.tool_calls = list(state.get('tool_calls', []))")
    lines.append("        result.steps = len(result.tool_calls)")
    return "\n".join(lines) + "\n"


# --------------------------------------------------------------------------- #
# eval_harness.py
# --------------------------------------------------------------------------- #


def _harness_module(spec: SystemSpec) -> str:
    cls = _class_name(spec.name)
    return f'''"""Run the generated eval suite against {cls}.

Usage:
    python eval_harness.py suite.json        # uses placeholder traces (dry run)
    # or import build_traces() and connect your real agent.
"""

from __future__ import annotations

import json
import sys

from agentic_ai_eval.schema import EvalSuite, Trace
from agentic_ai_eval.evals import run_suite
from agentic_ai_eval.report import render_markdown

from agent import {cls}


def build_traces(suite: EvalSuite) -> dict[str, Trace]:
    """Run the real agent over every case and capture a Trace per case."""
    agent = {cls}()
    traces: dict[str, Trace] = {{}}
    for ev in suite.evals:
        for case in ev.cases:
            try:
                r = agent.run(case.input)
                traces[case.id] = Trace(
                    case_id=case.id,
                    output=r.output,
                    tool_calls=r.tool_calls,
                    steps=r.steps,
                    input_tokens=r.input_tokens,
                    output_tokens=r.output_tokens,
                )
            except NotImplementedError:
                # Agent not implemented yet — leave a placeholder trace.
                traces[case.id] = Trace(case_id=case.id, output="", steps=0)
    return traces


def main() -> int:
    suite_path = sys.argv[1] if len(sys.argv) > 1 else "suite.json"
    suite = EvalSuite.model_validate_json(open(suite_path).read())
    traces = build_traces(suite)
    report = run_suite(suite, traces)
    print(render_markdown(report))
    return 0 if report.passed else 1


if __name__ == "__main__":
    raise SystemExit(main())
'''


def _readme(spec: SystemSpec) -> str:
    comps = "\n".join(f"- **{c.name}** (`{c.type.value}`): {c.description or '—'}" for c in spec.components)
    return f"""# Generated scaffold: {spec.name}

{spec.summary or spec.goal}

## Components
{comps}

## Next steps
1. Implement the `NotImplementedError` methods in `agent.py`.
2. Run the eval harness: `python eval_harness.py suite.json`.
3. Iterate until the suite passes. Wire it into CI as a gate.
"""


# --------------------------------------------------------------------------- #
# helpers
# --------------------------------------------------------------------------- #


def _class_name(name: str) -> str:
    parts = re.split(r"[^A-Za-z0-9]+", name)
    cls = "".join(p.capitalize() for p in parts if p) or "Agent"
    if not cls.endswith("Agent"):
        cls += "Agent"
    if cls[0].isdigit():
        cls = "A" + cls
    return cls


def _safe_ident(s: str) -> str:
    ident = re.sub(r"[^a-zA-Z0-9_]", "_", s).strip("_") or "step"
    if ident[0].isdigit():
        ident = "_" + ident
    if keyword.iskeyword(ident):
        ident += "_"
    return ident
